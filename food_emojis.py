def _normalize_phrase(text: str) -> str:
    return " ".join((text or "").strip().casefold().split())


def _tokenize(text: str) -> list[str]:
    raw = (text or "").strip().casefold()
    parts = re.split(r"[^\w\dё]+", raw, flags=re.UNICODE)
    return [p for p in parts if p]


def pick_emoji(name: str, *, default: str) -> str:
    """
    Универсальный подбор: точное совпадение фразы, затем самое длинное вхождение
    ключа как подстроки (специфичнее, чем одно короткое слово вроде «масло»),
    затем совпадение отдельных слов.
    """
    key = _normalize_phrase(name)
    if not key:
        return default

    if key in _EMOJI_MAP:
        return _EMOJI_MAP[key]

    best_emoji: str | None = None
    best_len = -1
    for phrase, emoji in _EMOJI_MAP.items():
        if phrase in key and len(phrase) > best_len:
            best_len = len(phrase)
            best_emoji = emoji

    if best_emoji is not None:
        return best_emoji

    for token in _tokenize(key):
        if token in _EMOJI_MAP:
            return _EMOJI_MAP[token]

    return default


def emoji_for_dish(name: str) -> str:
    """Эмодзи для названия блюда из базы или ввода пользователя."""
    return pick_emoji(name, default=_DEFAULT_DISH)


def emoji_for_ingredient(name: str) -> str:
    """Эмодзи для строки ингредиента."""
    return pick_emoji(name, default=_DEFAULT_INGREDIENT)
